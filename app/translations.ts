// Real, hand-maintained translations - replaces the old Google Translate
// widget approach, which worked by walking the live DOM and swapping text
// nodes after the fact. That fundamentally fights against how React
// re-renders (the widget's own interval had to keep re-applying itself
// every 800ms just to catch up), and it has no way to know which text
// should stay untranslated vs which is real content - hence the "translates
// everything except the English words" symptom. This is a plain lookup
// table instead: components ask for a string by key, get back the right
// language's version, done. No DOM walking, no fighting React, no
// inconsistent partial-translation.
//
// Currently covers English (baseline), Korean, and Spanish - the three
// requested as the starting set. Adding another language later is just
// adding another object below with the same keys; no code changes needed
// elsewhere once a component is wired up to use t() instead of a literal
// string.

